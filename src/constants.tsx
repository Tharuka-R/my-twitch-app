export const createStreamId = (date: string, streamerName: string, streamTitle: string): string => {
  const datePart = date.replace(/-/g, '');
  const namePart = streamerName.trim().toLowerCase().replace(/\s+/g, '-');
  const titlePart = streamTitle.trim().toLowerCase().replace(/\s+/g, '-');
  return `${datePart}_${namePart}_${titlePart}`;
};
